// Helper functions
export const getFeaturedPosts = () => {
  return blogPosts.filter(post => post.featured);
};

export const getPostsByCategory = (category) => {
  if (category === "All Posts") return blogPosts;
  return blogPosts.filter(post => post.category === category);
};

export const getPostBySlug = (slug) => {
  return blogPosts.find(post => post.slug === slug);
};

export const getLatestPosts = (count = 3) => {
  return blogPosts
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, count);
};

export const getRecentBlogPosts = (count = 3) => {
  return getLatestPosts(count);
};

export const getFeaturedBlogPost = () => {
  return blogPosts.find(post => post.featured) || blogPosts[0];
};

export const getBlogCategories = () => {
  return categories;
};

export const getRelatedPosts = (currentPost, count = 3) => {
  return blogPosts
    .filter(post =>
      post.id !== currentPost.id &&
      (post.category === currentPost.category ||
        post.tags.some(tag => currentPost.tags.includes(tag)))
    )
    .slice(0, count);
};

export const searchPosts = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return blogPosts.filter(post =>
    post.title.toLowerCase().includes(lowercaseQuery) ||
    post.excerpt.toLowerCase().includes(lowercaseQuery) ||
    post.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export default blogPosts;

